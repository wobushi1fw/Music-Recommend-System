package com.example.music_recommend_system.entity;

public class Artist {
    private Integer id;  // 艺术家ID
    private String name;  // 艺术家名字

    // Getters and Setters
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
